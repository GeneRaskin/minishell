#include "get_next_line.h"

int	ft_strchr_alt(const char *s, int c)
{
	int	idx;
	int	i;

	idx = -1;
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			idx = i;
			break ;
		}
		i++;
	}
	if (s[i] == c)
		idx = i;
	return (idx);
}
